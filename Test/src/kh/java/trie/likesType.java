package kh.java.trie;

public class likesType {
	int num;
	char type;
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public char getType() {
		return type;
	}
	public void setType(char type) {
		this.type = type;
	}
	public likesType(int num, char type) {
		super();
		this.num = num;
		this.type = type;
	}
	
}
